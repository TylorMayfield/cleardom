import { promises as fs } from "node:fs";
import * as path from "node:path";
import { writeEvidenceFragment } from "./evidence-fragment.mjs";

const [category, input] = process.argv.slice(2);
if (!category || !input) throw new Error("Usage: node scripts/record-evidence-fragment.mjs <category> <measured-values.json>");
const root = process.cwd();
const gates = JSON.parse(await fs.readFile(path.join(root, "release-gates.json"), "utf8"));
if (!(gates.requiredEvidenceFragments ?? []).includes(category)) throw new Error(`Unknown release evidence category: ${category}.`);
if (["precision", "conformance-source", "conformance-runtime"].includes(category)) throw new Error(`${category} is generated by its owning test job and cannot be manually recorded.`);
const values = JSON.parse(await fs.readFile(path.resolve(input), "utf8"));
if (!values || typeof values !== "object" || Array.isArray(values)) throw new Error("Measured evidence values must be a JSON object.");
await writeEvidenceFragment(root, `${category}.json`, category, values);
console.log(`Recorded same-commit ${category} evidence from ${input}.`);
