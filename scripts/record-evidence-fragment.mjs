import { promises as fs } from "node:fs";
import * as path from "node:path";
import { resolveReleaseStage, writeEvidenceFragment } from "./evidence-fragment.mjs";

const args = process.argv.slice(2).filter((arg) => arg !== "--");
const stageFlag = args.indexOf("--stage");
const requestedStage = stageFlag >= 0 ? args[stageFlag + 1] : undefined;
if (stageFlag >= 0) args.splice(stageFlag, 2);
const [category, input] = args;
if (!category || !input) throw new Error("Usage: node scripts/record-evidence-fragment.mjs <category> <measured-values.json> [--stage <stage>]");
const root = process.cwd();
const gates = JSON.parse(await fs.readFile(path.join(root, "release-gates.json"), "utf8"));
const knownCategories = new Set(Object.values(gates.requiredEvidenceFragmentsByStage ?? {}).flat());
if (!knownCategories.has(category)) throw new Error(`Unknown release evidence category: ${category}.`);
if (["contracts", "precision", "conformance-source", "conformance-runtime"].includes(category)) throw new Error(`${category} is generated by its owning test job and cannot be manually recorded.`);
const values = JSON.parse(await fs.readFile(path.resolve(input), "utf8"));
if (!values || typeof values !== "object" || Array.isArray(values)) throw new Error("Measured evidence values must be a JSON object.");
await writeEvidenceFragment(root, `${category}.json`, category, values, resolveReleaseStage(requestedStage));
console.log(`Recorded same-commit ${category} evidence from ${input}.`);
